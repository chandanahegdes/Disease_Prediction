import pandas as pd
df=pd.read_csv("/content/training_data.csv")
x_train=df.iloc[:,:132]
y_train =df.iloc[:,132:133]
df1=pd.read_csv("/content/test_data.csv")
x_test=df1.iloc[:,:132]
y_test =df1.iloc[:,132:133]

from sklearn.naive_bayes import MultinomialNB
model1=MultinomialNB()
model1.fit(x_train, y_train)
model1.score(x_test,y_test)

pred1 = model1.predict(x_test)

from sklearn.tree import DecisionTreeClassifier
model2=DecisionTreeClassifier(random_state=0)
model2.fit(x_train,y_train)
model2.score(x_test,y_test)

pred2=model2.predict(x_test)

from sklearn.svm import SVC
model3=SVC(random_state=2)
model3.fit(x_train,y_train)
model3.score(x_test,y_test)

pred3=model3.predict(x_test)

print(len(pred1))
print(len(pred2))
print(len(pred3))
print(len(y_test))

import collections
pred5=[]
for i in range(len(y_test)):
    pred4=[]
    pred4.append(pred1[i])
    pred4.append(pred2[i])
    pred4.append(pred3[i])
    frequency = collections.Counter(pred4)
    # pred5=dict(frequency)
    max_val=max(frequency)
    pred5.append(max_val)
    
print(pred5)